// MARK:- UIColor
extension UIColor {
    static func getColor(_ hex: String) -> UIColor {
        
        var cString:String = hex.trimmingCharacters(in: .whitespacesAndNewlines).uppercased()
        
        if (cString.hasPrefix("#")) {
            cString.remove(at: cString.startIndex)
        }
        
        if ((cString.count) != 6) {
            return UIColor.gray
        }
        
        var rgbValue:UInt32 = 0
        Scanner(string: cString).scanHexInt32(&rgbValue)
        
        return UIColor(
            red: CGFloat((rgbValue & 0xFF0000) >> 16) / 255.0,
            green: CGFloat((rgbValue & 0x00FF00) >> 8) / 255.0,
            blue: CGFloat(rgbValue & 0x0000FF) / 255.0,
            alpha: CGFloat(1.0)
        )
    }
    
    static func getRandomColor() -> UIColor{
        let randomRed:CGFloat = CGFloat(drand48())
        let randomGreen:CGFloat = CGFloat(drand48())
        let randomBlue:CGFloat = CGFloat(drand48())
        
        return UIColor(red: randomRed, green: randomGreen, blue: randomBlue, alpha: 1.0)
    }
}

// MARK:- Date
extension Date {
    static func getSeoulDateTime() -> Date {
        
        let curDate = Date()
        let dfTime = DateFormatter()
        dfTime.timeZone = TimeZone(identifier: "Asia/Seoul")
        dfTime.dateFormat = "yyyyMMddHHmmss"
        
        let strSeoulTime = dfTime.string(from: curDate)
        
        let dfSeoult = DateFormatter()
        dfSeoult.date(from: "yyyyMMddHHmmss")
        dfSeoult.locale = Locale(identifier: "ko_KR")
        
        let dtSeoul = dfSeoult.date(from: strSeoulTime)
        return dtSeoul!
    }
}

// MARK:- NSLayoutConstraint
extension NSLayoutConstraint {
    func setMultiplier(multiplier:CGFloat) -> NSLayoutConstraint {
        
        NSLayoutConstraint.deactivate([self])
        
        let newConstraint = NSLayoutConstraint(
            item: self.firstItem!,
            attribute: firstAttribute,
            relatedBy: relation,
            toItem: self.secondItem,
            attribute: secondAttribute,
            multiplier: multiplier,
            constant: constant)
        
        newConstraint.priority = priority
        newConstraint.shouldBeArchived = self.shouldBeArchived
        newConstraint.identifier = self.identifier
        
        NSLayoutConstraint.activate([newConstraint])
        return newConstraint
    }
}

